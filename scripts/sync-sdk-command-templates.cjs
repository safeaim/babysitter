function buildTemplate(name, commandMarkdown) {
  const parsed = parseFrontmatter(commandMarkdown);
  const builder = TEMPLATE_BUILDERS[name];
  if (!builder) {
    throw new Error(`No SDK command template builder is configured for ${name}.`);
  }

  return normalizeNewlines(
    builder({
      body: parsed.body.trim(),
      description: parsed.data.description || `Babysitter ${name} mode.`,
    }).join('\n\n').trim() + '\n',
  );
}

function findObsoleteCleanupPath(content) {
  return OBSOLETE_CLEANUP_PATH_PATTERNS.some((pattern) => pattern.test(content));
}

function assertNoObsoleteCleanupPath(label, content) {
  if (findObsoleteCleanupPath(content)) {
    throw new Error(`${label} references the obsolete cleanup cradle process path`);
  }
}

function main() {
  const stale = [];
  let updated = 0;

  for (const name of Object.keys(TEMPLATE_BUILDERS).sort()) {
    const sourcePath = path.join(SOURCE_ROOT, `${name}.md`);
    const targetPath = path.join(TARGET_ROOT, `${name}.md`);

    if (!fs.existsSync(sourcePath)) {
      throw new Error(`Missing source command markdown for ${name}: ${sourcePath}`);
    }

    const source = fs.readFileSync(sourcePath, 'utf8');
    if (name === 'cleanup') {
      assertNoObsoleteCleanupPath(path.relative(REPO_ROOT, sourcePath), source);
    }

    const expected = buildTemplate(name, source);
    const actual = fs.existsSync(targetPath)
      ? normalizeNewlines(fs.readFileSync(targetPath, 'utf8'))
      : null;

    if (CHECK) {
      if (name === 'cleanup' && actual) {
        assertNoObsoleteCleanupPath(path.relative(REPO_ROOT, targetPath), actual);
      }
      if (actual !== expected) {
        stale.push(path.relative(REPO_ROOT, targetPath));
      }
      continue;
    }

    if (writeFileIfChanged(targetPath, expected)) {
      updated += 1;
      console.log(`[${LABEL}] updated ${path.relative(REPO_ROOT, targetPath)}`);
    }
  }

  if (CHECK) {
    reportCheckResult(LABEL, stale);
    return;
  }

  if (updated === 0) {
    console.log(`[${LABEL}] no SDK command template changes were needed.`);
    return;
  }

  console.log(`[${LABEL}] updated ${updated} SDK command template file(s).`);
}

main();
